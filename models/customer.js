const mongoose = require("mongoose");

const customerSchema = new mongoose.Schema({
  userName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const customer = mongoose.model("Customer", customerSchema);

module.exports = customer;
